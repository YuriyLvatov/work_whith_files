package quru.qa.pojos;

public class Tasks {

    private int task;
    private String name;
    private String user;
    private String unit;
    private int sla;
    private boolean work;

    public int getTask() {
        return task;
    }

    public String getName() {
        return name;
    }

    public String getUser() {
        return user;
    }

    public String getUnit() {
        return unit;
    }

    public int getSla() {
        return sla;
    }

    public boolean isWork() {
        return work;
    }

}
